def sum_numbers(numbers) -> int:
    return sum(numbers)

def main():
    numbers = []
    n = int(input("How many numbers do you want to enter? "))
    for i in range(n):
        num = float(input(f"Enter number {i + 1}: "))
        numbers.append(num)

    result = sum_numbers(numbers)
    print("The sum of the numbers is:", result)

